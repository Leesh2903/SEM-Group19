package com.napier.sem;

/**
 * Class that represents a Country.
 *
 * @author  Lee Shaw
 * @version 1.0
 * @since   2021-02-17
 */
public class Country
{
    /**
     * Country Code
     */
    public String code;

    /**
     * Country Name
     */
    public String name;

    /**
     * Continent
     */
    public String continent;

    /**
     * Country Region
     */
    public String region;
    /**
     * Population
     */
    public int population;

    /**
     * Capital
     */
    public String capital;

}